// Función para mostrar productos en la página
function mostrarProductos(productos) {
    const contenedorProductos = document.getElementById("contenedor-productos");
    contenedorProductos.innerHTML = ""; // Limpia el contenedor antes de agregar nuevos productos

    productos.forEach(producto => {
        const productoItem = document.createElement("div");
        productoItem.classList.add("product-item");
        let contenidoProducto = `
            <img src="${producto.image}" alt="${producto.name}">
            <p><strong>${producto.name}</strong></p>
            <p>Precio: ${producto.price}</p>
        `;
        if (producto.colors) {
            contenidoProducto += `<p>Colores: ${producto.colors.join(", ")}</p>`;
        }
        if(producto.Talla){
            contenidoProducto += `<p>Talla: ${producto.Talla.join(", ")}</p>`;
        }
        productoItem.innerHTML = contenidoProducto;
        contenedorProductos.appendChild(productoItem);
    });
}


// Manejar clics en el menú de navegación
document.querySelectorAll(".navbar ul li a").forEach(link => {
    link.addEventListener("click", function (e) {
        e.preventDefault();
        const categoria = this.getAttribute("href").substring(1); // Obtiene la categoría (cadenas, pulseras, etc.)

        // Llama a la función mostrarProductos con los datos de la categoría seleccionada
        switch (categoria) {
            case "cadenas":
                mostrarProductos(cadenas);
                break;
            case "pulseras":
                mostrarProductos(pulseras);
                break;
            case "aretes":
                mostrarProductos(aretes);
                break;
            case "anillos":
                mostrarProductos(anillos);
                break;
           
            case "perfumes":
                mostrarProductos(perfumes);
                break;
            case "otros":
                mostrarProductos(otros);
                break;
        }
    });
});

// Muestra los productos de inicio (por ejemplo, cuando se carga la página)
